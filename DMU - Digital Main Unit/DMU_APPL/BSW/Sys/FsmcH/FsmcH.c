void FsmcH_MainFunction(void)
{
	FsmcH_Status = HAL_SRAM_GetState(&hsram1);
	if(4 == FsmcH_Status)
	{
		DigitalCluster_FsmcDmaErrorFlag = 1;
		FsmcH_InitRetry++;
		MX_FSMC_Init();
	}
	else DigitalCluster_FsmcDmaErrorFlag = 0;
	FsmcH_MainCounter++;
}

