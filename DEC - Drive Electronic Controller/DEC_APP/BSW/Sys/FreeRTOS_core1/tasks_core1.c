void vTaskStartScheduler_core1( void )
{
    BaseType_t_core1 xReturn_core1;

    /* Add the idle task_core1 at the lowest priority. */
    #if ( configSUPPORT_STATIC_ALLOCATION_core1 == 1 )
    {
        StaticTask_t_core1 * pxIdleTaskTCBBuffer_core1 = NULL;
        StackType_t_core1 * pxIdleTaskStackBuffer_core1 = NULL;
        uint32_t ulIdleTaskStackSize_core1;

        /* The Idle task_core1 is created using user provided RAM - obtain the
         * address of the RAM then create the idle task_core1. */
        vApplicationGetIdleTaskMemory_core1( &pxIdleTaskTCBBuffer_core1, &pxIdleTaskStackBuffer_core1, &ulIdleTaskStackSize_core1 );
        xIdleTaskHandle_core1 = xTaskCreateStatic_core1( prvIdleTask_core1,
                                             configIDLE_TASK_NAME_core1,
                                             ulIdleTaskStackSize_core1,
                                             ( void * ) NULL,       /*lint !e961.  The cast is not redundant for all compilers. */
                                             portPRIVILEGE_BIT_core1,     /* In effect ( tskIDLE_PRIORITY_core1 | portPRIVILEGE_BIT_core1 ), but tskIDLE_PRIORITY_core1 is zero. */
                                             pxIdleTaskStackBuffer_core1,
                                             pxIdleTaskTCBBuffer_core1 ); /*lint !e961 MISRA exception, justified as it is not a redundant explicit cast to all supported compilers. */

        if( xIdleTaskHandle_core1 != NULL )
        {
            xReturn_core1 = pdPASS_core1;
        }
        else
        {
            xReturn_core1 = pdFAIL_core1;
        }
    }
    #else /* if ( configSUPPORT_STATIC_ALLOCATION_core1 == 1 ) */
    {
        /* The Idle task_core1 is being created using dynamically allocated RAM. */
        xReturn_core1 = xTaskCreate_core1( prvIdleTask_core1,
                               configIDLE_TASK_NAME_core1,
                               configMINIMAL_STACK_SIZE_core1,
                               ( void * ) NULL,
                               portPRIVILEGE_BIT_core1,  /* In effect ( tskIDLE_PRIORITY_core1 | portPRIVILEGE_BIT_core1 ), but tskIDLE_PRIORITY_core1 is zero. */
                               &xIdleTaskHandle_core1 ); /*lint !e961 MISRA exception, justified as it is not a redundant explicit cast to all supported compilers. */
    }
    #endif /* configSUPPORT_STATIC_ALLOCATION_core1 */

    #if ( configUSE_TIMERS_core1 == 1 )
    {
        if( xReturn_core1 == pdPASS_core1 )
        {
            xReturn_core1 = xTimerCreateTimerTask_core1();
        }
        else
        {
            mtCOVERAGE_TEST_MARKER_core1();
        }
    }
    #endif /* configUSE_TIMERS_core1 */

    if( xReturn_core1 == pdPASS_core1 )
    {
        /* freertos_tasks_c_additions_init_core1() should only be called if the user
         * definable macro FREERTOS_TASKS_C_ADDITIONS_INIT_core1() is defined, as that is
         * the only macro called by the function. */
        #ifdef FREERTOS_TASKS_C_ADDITIONS_INIT_core1
        {
            freertos_tasks_c_additions_init_core1();
        }
        #endif

        /* Interrupts are turned off here, to ensure a tick does not occur
         * before or during the call to xPortStartScheduler_core1().  The stacks of
         * the created tasks contain a status word with interrupts switched on
         * so interrupts will automatically get re-enabled when the first task_core1
         * starts to run. */
        portDISABLE_INTERRUPTS_core1();

        #if ( ( configUSE_NEWLIB_REENTRANT_core1 == 1 ) || ( configUSE_C_RUNTIME_TLS_SUPPORT_core1 == 1 ) )
        {
            /* Switch C-Runtime's TLS Block to point to the TLS
             * block specific to the task_core1 that will run first. */
            configSET_TLS_BLOCK_core1( pxCurrentTCB_core1->xTLSBlock_core1 );
        }
        #endif

        xNextTaskUnblockTime_core1 = portMAX_DELAY_core1;
        xSchedulerRunning_core1 = pdTRUE_core1;
        xTickCount_core1 = ( TickType_t_core1 ) configINITIAL_TICK_COUNT_core1;

        /* If configGENERATE_RUN_TIME_STATS_core1 is defined then the following
         * macro must be defined to configure the timer/counter used to generate
         * the run time counter time base.   NOTE:  If configGENERATE_RUN_TIME_STATS_core1
         * is set to 0 and the following line fails to build then ensure you do not
         * have portCONFIGURE_TIMER_FOR_RUN_TIME_STATS_core1() defined in your
         * FreeRTOSConfig.h file. */
        //portCONFIGURE_TIMER_FOR_RUN_TIME_STATS_core1();

        traceTASK_SWITCHED_IN_core1();

        /* Setting up the timer tick is hardware specific and thus in the
         * portable interface. */
        xPortStartScheduler_core1();

        /* In most cases, xPortStartScheduler_core1() will not return. If it
         * returns pdTRUE_core1 then there was not enough heap memory available
         * to create either the Idle or the Timer task_core1. If it returned
         * pdFALSE_core1, then the application called xTaskEndScheduler().
         * Most ports don't implement xTaskEndScheduler() as there is
         * nothing to return to. */
    }
    else
    {
        /* This line will only be reached if the kernel could not be started,
         * because there was not enough FreeRTOS_core1 heap to create the idle task_core1
         * or the timer task_core1. */
        configASSERT_core1( xReturn_core1 != errCOULD_NOT_ALLOCATE_REQUIRED_MEMORY_core1 );
    }

    /* Prevent compiler warnings if INCLUDE_xTaskGetIdleTaskHandle_core1 is set to 0,
     * meaning xIdleTaskHandle_core1 is not used anywhere else. */
    ( void ) xIdleTaskHandle_core1;

    /* OpenOCD makes use of uxTopUsedPriority_core1 for thread debugging. Prevent uxTopUsedPriority_core1
     * from getting optimized out as it is no longer used by the kernel. */
    ( void ) uxTopUsedPriority_core1;
}
