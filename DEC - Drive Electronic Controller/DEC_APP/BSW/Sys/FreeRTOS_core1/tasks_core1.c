void vTaskSuspendAll_core1( void )
{
    /* A critical section is not required as the variable is of type
     * BaseType_t_core1.  Please read Richard Barry's reply in the following link to a
     * post in the FreeRTOS_core1 support forum before reporting this as a bug! -
     * https://goo.gl/wu4acr */

    /* portSOFTWARE_BARRIER_core1() is only implemented for emulated/simulated ports that
     * do not otherwise exhibit real time behaviour. */
    portSOFTWARE_BARRIER_core1();

    /* The scheduler is suspended if uxSchedulerSuspended_core1 is non-zero.  An increment
     * is used to allow calls to vTaskSuspendAll_core1() to nest. */
    ++uxSchedulerSuspended_core1;

    /* Enforces ordering for ports and optimised compilers that may otherwise place
     * the above increment elsewhere. */
    portMEMORY_BARRIER_core1();
}
