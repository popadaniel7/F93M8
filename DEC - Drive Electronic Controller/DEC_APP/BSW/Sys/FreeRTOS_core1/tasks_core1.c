#if ( INCLUDE_vTaskSuspend_core1 == 1 )

    void vTaskResume_core1( TaskHandle_t_core1 xTaskToResume_core1 )
    {
        TCB_t_core1 * const pxTCB_core1 = xTaskToResume_core1;

        /* It does not make sense to resume the calling task_core1. */
        configASSERT_core1( xTaskToResume_core1 );

        /* The parameter cannot be NULL as it is impossible to resume the
         * currently executing task_core1. */
        if( ( pxTCB_core1 != pxCurrentTCB_core1 ) && ( pxTCB_core1 != NULL ) )
        {
            taskENTER_CRITICAL_core1();
            {
                if( prvTaskIsTaskSuspended_core1( pxTCB_core1 ) != pdFALSE_core1 )
                {
                    traceTASK_RESUME_core1( pxTCB_core1 );

                    /* The ready list can be accessed even if the scheduler is
                     * suspended because this is inside a critical section. */
                    ( void ) uxListRemove_core1( &( pxTCB_core1->xStateListItem_core1 ) );
                    prvAddTaskToReadyList_core1( pxTCB_core1 );

                    /* A higher priority task_core1 may have just been resumed. */
                    if( pxTCB_core1->uxPriority_core1 >= pxCurrentTCB_core1->uxPriority_core1 )
                    {
                        /* This yield may not cause the task_core1 just resumed to run,
                         * but will leave the lists in the correct state for the
                         * next yield. */
                        taskYIELD_IF_USING_PREEMPTION_core1();
                    }
                    else
                    {
                        mtCOVERAGE_TEST_MARKER_core1();
                    }
                }
                else
                {
                    mtCOVERAGE_TEST_MARKER_core1();
                }
            }
            taskEXIT_CRITICAL_core1();
        }
        else
        {
            mtCOVERAGE_TEST_MARKER_core1();
        }
    }

#endif /* INCLUDE_vTaskSuspend_core1 */

