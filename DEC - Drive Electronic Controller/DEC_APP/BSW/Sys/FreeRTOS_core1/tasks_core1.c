#if ( INCLUDE_vTaskPrioritySet_core1 == 1 )

    void vTaskPrioritySet_core1( TaskHandle_t_core1 xTask_core1,
                           UBaseType_t_core1 uxNewPriority_core1 )
    {
        TCB_t_core1 * pxTCB_core1;
        UBaseType_t_core1 uxCurrentBasePriority_core1, uxPriorityUsedOnEntry_core1;
        BaseType_t_core1 xYieldRequired_core1 = pdFALSE_core1;

        configASSERT_core1( uxNewPriority_core1 < configMAX_PRIORITIES_core1 );

        /* Ensure the new priority is valid. */
        if( uxNewPriority_core1 >= ( UBaseType_t_core1 ) configMAX_PRIORITIES_core1 )
        {
            uxNewPriority_core1 = ( UBaseType_t_core1 ) configMAX_PRIORITIES_core1 - ( UBaseType_t_core1 ) 1U;
        }
        else
        {
            mtCOVERAGE_TEST_MARKER_core1();
        }

        taskENTER_CRITICAL_core1();
        {
            /* If null is passed in here then it is the priority of the calling
             * task_core1 that is being changed. */
            pxTCB_core1 = prvGetTCBFromHandle_core1( xTask_core1 );

            traceTASK_PRIORITY_SET_core1( pxTCB_core1, uxNewPriority_core1 );

            #if ( configUSE_MUTEXES_core1 == 1 )
            {
                uxCurrentBasePriority_core1 = pxTCB_core1->uxBasePriority_core1;
            }
            #else
            {
                uxCurrentBasePriority_core1 = pxTCB_core1->uxPriority_core1;
            }
            #endif

            if( uxCurrentBasePriority_core1 != uxNewPriority_core1 )
            {
                /* The priority change may have readied a task_core1 of higher
                 * priority than the calling task_core1. */
                if( uxNewPriority_core1 > uxCurrentBasePriority_core1 )
                {
                    if( pxTCB_core1 != pxCurrentTCB_core1 )
                    {
                        /* The priority of a task_core1 other than the currently
                         * running task_core1 is being raised.  Is the priority being
                         * raised above that of the running task_core1? */
                        if( uxNewPriority_core1 >= pxCurrentTCB_core1->uxPriority_core1 )
                        {
                            xYieldRequired_core1 = pdTRUE_core1;
                        }
                        else
                        {
                            mtCOVERAGE_TEST_MARKER_core1();
                        }
                    }
                    else
                    {
                        /* The priority of the running task_core1 is being raised,
                         * but the running task_core1 must already be the highest
                         * priority task_core1 able to run so no yield is required. */
                    }
                }
                else if( pxTCB_core1 == pxCurrentTCB_core1 )
                {
                    /* Setting the priority of the running task_core1 down means
                     * there may now be another task_core1 of higher priority that
                     * is ready to execute. */
                    xYieldRequired_core1 = pdTRUE_core1;
                }
                else
                {
                    /* Setting the priority of any other task_core1 down does not
                     * require a yield as the running task_core1 must be above the
                     * new priority of the task_core1 being modified. */
                }

                /* Remember the ready list the task_core1 might be referenced from
                 * before its uxPriority_core1 member is changed so the
                 * taskRESET_READY_PRIORITY_core1() macro can function correctly. */
                uxPriorityUsedOnEntry_core1 = pxTCB_core1->uxPriority_core1;

                #if ( configUSE_MUTEXES_core1 == 1 )
                {
                    /* Only change the priority being used if the task_core1 is not
                     * currently using an inherited priority. */
                    if( pxTCB_core1->uxBasePriority_core1 == pxTCB_core1->uxPriority_core1 )
                    {
                        pxTCB_core1->uxPriority_core1 = uxNewPriority_core1;
                    }
                    else
                    {
                        mtCOVERAGE_TEST_MARKER_core1();
                    }

                    /* The base priority gets set whatever. */
                    pxTCB_core1->uxBasePriority_core1 = uxNewPriority_core1;
                }
                #else /* if ( configUSE_MUTEXES_core1 == 1 ) */
                {
                    pxTCB_core1->uxPriority_core1 = uxNewPriority_core1;
                }
                #endif /* if ( configUSE_MUTEXES_core1 == 1 ) */

                /* Only reset the event list item value if the value is not
                 * being used for anything else. */
                if( ( listGET_LIST_ITEM_VALUE_core1( &( pxTCB_core1->xEventListItem_core1 ) ) & taskEVENT_LIST_ITEM_VALUE_IN_USE_core1 ) == 0UL )
                {
                    listSET_LIST_ITEM_VALUE_core1( &( pxTCB_core1->xEventListItem_core1 ), ( ( TickType_t_core1 ) configMAX_PRIORITIES_core1 - ( TickType_t_core1 ) uxNewPriority_core1 ) ); /*lint !e961 MISRA exception as the casts are only redundant for some ports. */
                }
                else
                {
                    mtCOVERAGE_TEST_MARKER_core1();
                }

                /* If the task_core1 is in the blocked or suspended list we need do
                 * nothing more than change its priority variable. However, if
                 * the task_core1 is in a ready list it needs to be removed and placed
                 * in the list appropriate to its new priority. */
                if( listIS_CONTAINED_WITHIN_core1( &( pxReadyTasksLists_core1[ uxPriorityUsedOnEntry_core1 ] ), &( pxTCB_core1->xStateListItem_core1 ) ) != pdFALSE_core1 )
                {
                    /* The task_core1 is currently in its ready list - remove before
                     * adding it to its new ready list.  As we are in a critical
                     * section we can do this even if the scheduler is suspended. */
                    if( uxListRemove_core1( &( pxTCB_core1->xStateListItem_core1 ) ) == ( UBaseType_t_core1 ) 0 )
                    {
                        /* It is known that the task_core1 is in its ready list so
                         * there is no need to check again and the port level
                         * reset macro can be called directly. */
                        portRESET_READY_PRIORITY_core1( uxPriorityUsedOnEntry_core1, uxTopReadyPriority_core1 );
                    }
                    else
                    {
                        mtCOVERAGE_TEST_MARKER_core1();
                    }

                    prvAddTaskToReadyList_core1( pxTCB_core1 );
                }
                else
                {
                    mtCOVERAGE_TEST_MARKER_core1();
                }

                if( xYieldRequired_core1 != pdFALSE_core1 )
                {
                    taskYIELD_IF_USING_PREEMPTION_core1();
                }
                else
                {
                    mtCOVERAGE_TEST_MARKER_core1();
                }

                /* Remove compiler warning about unused variables when the port
                 * optimised task_core1 selection is not being used. */
                ( void ) uxPriorityUsedOnEntry_core1;
            }
        }
        taskEXIT_CRITICAL_core1();
    }

#endif /* INCLUDE_vTaskPrioritySet_core1 */
