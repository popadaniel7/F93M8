#if ( INCLUDE_vTaskDelete_core1 == 1 )

    void vTaskDelete_core1( TaskHandle_t_core1 xTaskToDelete_core1 )
    {
        TCB_t_core1 * pxTCB_core1;

        taskENTER_CRITICAL_core1();
        {
            /* If null is passed in here then it is the calling task_core1 that is
             * being deleted. */
            pxTCB_core1 = prvGetTCBFromHandle_core1( xTaskToDelete_core1 );

            /* Remove task_core1 from the ready/delayed list. */
            if( uxListRemove_core1( &( pxTCB_core1->xStateListItem_core1 ) ) == ( UBaseType_t_core1 ) 0 )
            {
                taskRESET_READY_PRIORITY_core1( pxTCB_core1->uxPriority_core1 );
            }
            else
            {
                mtCOVERAGE_TEST_MARKER_core1();
            }

            /* Is the task_core1 waiting on an event also? */
            if( listLIST_ITEM_CONTAINER_core1( &( pxTCB_core1->xEventListItem_core1 ) ) != NULL )
            {
                ( void ) uxListRemove_core1( &( pxTCB_core1->xEventListItem_core1 ) );
            }
            else
            {
                mtCOVERAGE_TEST_MARKER_core1();
            }

            /* Increment the uxTaskNumber_core1 also so kernel aware debuggers can
             * detect that the task_core1 lists need re-generating.  This is done before
             * portPRE_TASK_DELETE_HOOK_core1() as in the Windows port that macro will
             * not return. */
            uxTaskNumber_core1++;

            if( pxTCB_core1 == pxCurrentTCB_core1 )
            {
                /* A task_core1 is deleting itself.  This cannot complete within the
                 * task_core1 itself, as a context switch to another task_core1 is required.
                 * Place the task_core1 in the termination list.  The idle task_core1 will
                 * check the termination list and free up any memory allocated by
                 * the scheduler for the TCB and stack of the deleted task_core1. */
                vListInsertEnd_core1( &xTasksWaitingTermination_core1, &( pxTCB_core1->xStateListItem_core1 ) );

                /* Increment the ucTasksDeleted variable so the idle task_core1 knows
                 * there is a task_core1 that has been deleted and that it should therefore
                 * check the xTasksWaitingTermination_core1 list. */
                ++uxDeletedTasksWaitingCleanUp_core1;

                /* Call the delete hook before portPRE_TASK_DELETE_HOOK_core1() as
                 * portPRE_TASK_DELETE_HOOK_core1() does not return in the Win32 port. */
                traceTASK_DELETE_core1( pxTCB_core1 );

                /* The pre-delete hook is primarily for the Windows simulator,
                 * in which Windows specific clean up operations are performed,
                 * after which it is not possible to yield away from this task_core1 -
                 * hence xYieldPending_core1 is used to latch that a context switch is
                 * required. */
                portPRE_TASK_DELETE_HOOK_core1( pxTCB_core1, &xYieldPending_core1 );
            }
            else
            {
                --uxCurrentNumberOfTasks_core1;
                traceTASK_DELETE_core1( pxTCB_core1 );

                /* Reset the next expected unblock time in case it referred to
                 * the task_core1 that has just been deleted. */
                prvResetNextTaskUnblockTime_core1();
            }
        }
        taskEXIT_CRITICAL_core1();

        /* If the task_core1 is not deleting itself, call prvDeleteTCB_core1 from outside of
         * critical section. If a task_core1 deletes itself, prvDeleteTCB_core1 is called
         * from prvCheckTasksWaitingTermination_core1 which is called from Idle task_core1. */
        if( pxTCB_core1 != pxCurrentTCB_core1 )
        {
            prvDeleteTCB_core1( pxTCB_core1 );
        }

        /* Force a reschedule if it is the currently running task_core1 that has just
         * been deleted. */
        if( xSchedulerRunning_core1 != pdFALSE_core1 )
        {
            if( pxTCB_core1 == pxCurrentTCB_core1 )
            {
                configASSERT_core1( uxSchedulerSuspended_core1 == 0 );
                portYIELD_WITHIN_API_core1();
            }
            else
            {
                mtCOVERAGE_TEST_MARKER_core1();
            }
        }
    }

#endif /* INCLUDE_vTaskDelete_core1 */
