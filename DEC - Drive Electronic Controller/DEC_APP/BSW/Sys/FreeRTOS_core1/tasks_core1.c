void vTaskPlaceOnUnorderedEventList_core1( List_t_core1 * pxEventList_core1,
                                     const TickType_t_core1 xItemValue_core1,
                                     const TickType_t_core1 xTicksToWait_core1 )
{
    configASSERT_core1( pxEventList_core1 );

    /* THIS FUNCTION MUST BE CALLED WITH THE SCHEDULER SUSPENDED.  It is used by
     * the event groups implementation. */
    configASSERT_core1( uxSchedulerSuspended_core1 != 0 );

    /* Store the item value in the event list item.  It is safe to access the
     * event list item here as interrupts won't access the event list item of a
     * task_core1 that is not in the Blocked state. */
    listSET_LIST_ITEM_VALUE_core1( &( pxCurrentTCB_core1->xEventListItem_core1 ), xItemValue_core1 | taskEVENT_LIST_ITEM_VALUE_IN_USE_core1 );

    /* Place the event list item of the TCB at the end of the appropriate event
     * list.  It is safe to access the event list here because it is part of an
     * event group implementation - and interrupts don't access event groups
     * directly (instead they access them indirectly by pending function calls to
     * the task_core1 level). */
    listINSERT_END_core1( pxEventList_core1, &( pxCurrentTCB_core1->xEventListItem_core1 ) );

    prvAddCurrentTaskToDelayedList_core1( xTicksToWait_core1, pdTRUE_core1 );
}
