void vTaskRemoveFromUnorderedEventList_core1( ListItem_t_core1 * pxEventListItem_core1,
                                        const TickType_t_core1 xItemValue_core1 )
{
    TCB_t_core1 * pxUnblockedTCB;

    /* THIS FUNCTION MUST BE CALLED WITH THE SCHEDULER SUSPENDED.  It is used by
     * the event flags implementation. */
    configASSERT_core1( uxSchedulerSuspended_core1 != pdFALSE_core1 );

    /* Store the new item value in the event list. */
    listSET_LIST_ITEM_VALUE_core1( pxEventListItem_core1, xItemValue_core1 | taskEVENT_LIST_ITEM_VALUE_IN_USE_core1 );

    /* Remove the event list form the event flag.  Interrupts do not access
     * event flags. */
    pxUnblockedTCB = listGET_LIST_ITEM_OWNER_core1( pxEventListItem_core1 ); /*lint !e9079 void * is used as this macro is used with timers_core1 and co-routines too.  Alignment is known to be fine as the type of the pointer stored and retrieved is the same. */
    configASSERT_core1( pxUnblockedTCB );
    listREMOVE_ITEM_core1( pxEventListItem_core1 );

    #if ( configUSE_TICKLESS_IDLE_core1 != 0 )
    {
        /* If a task_core1 is blocked on a kernel object then xNextTaskUnblockTime_core1
         * might be set to the blocked task_core1's time out time.  If the task_core1 is
         * unblocked for a reason other than a timeout xNextTaskUnblockTime_core1 is
         * normally left unchanged, because it is automatically reset to a new
         * value when the tick count equals xNextTaskUnblockTime_core1.  However if
         * tickless idling is used it might be more important to enter sleep mode
         * at the earliest possible time - so reset xNextTaskUnblockTime_core1 here to
         * ensure it is updated at the earliest possible time. */
        prvResetNextTaskUnblockTime_core1();
    }
    #endif

    /* Remove the task_core1 from the delayed list and add it to the ready list.  The
     * scheduler is suspended so interrupts will not be accessing the ready
     * lists. */
    listREMOVE_ITEM_core1( &( pxUnblockedTCB->xStateListItem_core1 ) );
    prvAddTaskToReadyList_core1( pxUnblockedTCB );

    if( pxUnblockedTCB->uxPriority_core1 > pxCurrentTCB_core1->uxPriority_core1 )
    {
        /* The unblocked task_core1 has a priority above that of the calling task_core1, so
         * a context switch is required.  This function is called with the
         * scheduler suspended so xYieldPending_core1 is set so the context switch
         * occurs immediately that the scheduler is resumed (unsuspended). */
        xYieldPending_core1 = pdTRUE_core1;
    }
}
