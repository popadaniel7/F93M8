#if ( configSUPPORT_DYNAMIC_ALLOCATION_core1 == 1 )

    BaseType_t_core1 xTaskCreate_core1( TaskFunction_t_core1 pxTaskCode_core1,
                            const char * const pcName_core1, /*lint !e971 Unqualified char types are allowed for strings and single characters only. */
                            const configSTACK_DEPTH_TYPE_core1 usStackDepth_core1,
                            void * const pvParameters_core1,
                            UBaseType_t_core1 uxPriority_core1,
                            TaskHandle_t_core1 * const pxCreatedTask_core1 )
    {
        TCB_t_core1 * pxNewTCB_core1;
        BaseType_t_core1 xReturn_core1;

        /* If the stack grows down then allocate the stack then the TCB so the stack
         * does not grow into the TCB.  Likewise if the stack grows up then allocate
         * the TCB then the stack. */
        #if ( portSTACK_GROWTH_core1 > 0 )
        {
            /* Allocate space for the TCB.  Where the memory comes from depends on
             * the implementation of the port malloc function and whether or not static
             * allocation is being used. */
            pxNewTCB_core1 = ( TCB_t_core1 * ) pvPortMalloc_core1( sizeof( TCB_t_core1 ) );

            if( pxNewTCB_core1 != NULL )
            {
                memset( ( void * ) pxNewTCB_core1, 0x00, sizeof( TCB_t_core1 ) );

                /* Allocate space for the stack used by the task_core1 being created.
                 * The base of the stack memory stored in the TCB so the task_core1 can
                 * be deleted later if required. */
                pxNewTCB_core1->pxStack_core1 = ( StackType_t_core1 * ) pvPortMallocStack_core1( ( ( ( size_t ) usStackDepth_core1 ) * sizeof( StackType_t_core1 ) ) ); /*lint !e961 MISRA exception as the casts are only redundant for some ports. */

                if( pxNewTCB_core1->pxStack_core1 == NULL )
                {
                    /* Could not allocate the stack.  Delete the allocated TCB. */
                    vPortFree_core1( pxNewTCB_core1 );
                    pxNewTCB_core1 = NULL;
                }
            }
        }
        #else /* portSTACK_GROWTH_core1 */
        {
            StackType_t_core1 * pxStack_core1;

            /* Allocate space for the stack used by the task_core1 being created. */
            pxStack_core1 = pvPortMallocStack_core1( ( ( ( size_t ) usStackDepth_core1 ) * sizeof( StackType_t_core1 ) ) ); /*lint !e9079 All values returned by pvPortMalloc_core1() have at least the alignment required by the MCU's stack and this allocation is the stack. */

            if( pxStack_core1 != NULL )
            {
                /* Allocate space for the TCB. */
                pxNewTCB_core1 = ( TCB_t_core1 * ) pvPortMalloc_core1( sizeof( TCB_t_core1 ) ); /*lint !e9087 !e9079 All values returned by pvPortMalloc_core1() have at least the alignment required by the MCU's stack, and the first member of TCB_t_core1 is always a pointer to the task_core1's stack. */

                if( pxNewTCB_core1 != NULL )
                {
                    memset( ( void * ) pxNewTCB_core1, 0x00, sizeof( TCB_t_core1 ) );

                    /* Store the stack location in the TCB. */
                    pxNewTCB_core1->pxStack_core1 = pxStack_core1;
                }
                else
                {
                    /* The stack cannot be used as the TCB was not created.  Free
                     * it again. */
                    vPortFreeStack_core1( pxStack_core1 );
                }
            }
            else
            {
                pxNewTCB_core1 = NULL;
            }
        }
        #endif /* portSTACK_GROWTH_core1 */

        if( pxNewTCB_core1 != NULL )
        {
            #if ( tskSTATIC_AND_DYNAMIC_ALLOCATION_POSSIBLE_core1 != 0 ) /*lint !e9029 !e731 Macro has been consolidated for readability reasons. */
            {
                /* Tasks can be created statically or dynamically, so note this
                 * task_core1 was created dynamically in case it is later deleted. */
                pxNewTCB_core1->ucStaticallyAllocated_core1 = tskDYNAMICALLY_ALLOCATED_STACK_AND_TCB_core1;
            }
            #endif /* tskSTATIC_AND_DYNAMIC_ALLOCATION_POSSIBLE_core1 */

            prvInitialiseNewTask_core1( pxTaskCode_core1, pcName_core1, ( uint32_t ) usStackDepth_core1, pvParameters_core1, uxPriority_core1, pxCreatedTask_core1, pxNewTCB_core1, NULL );
            prvAddNewTaskToReadyList_core1( pxNewTCB_core1 );
            xReturn_core1 = pdPASS_core1;
        }
        else
        {
            xReturn_core1 = errCOULD_NOT_ALLOCATE_REQUIRED_MEMORY_core1;
        }

        return xReturn_core1;
    }

#endif /* configSUPPORT_DYNAMIC_ALLOCATION_core1 */
