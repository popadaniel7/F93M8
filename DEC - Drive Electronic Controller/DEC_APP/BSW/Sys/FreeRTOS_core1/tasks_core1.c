#if ( configUSE_MUTEXES_core1 == 1 )

    void vTaskPriorityDisinheritAfterTimeout_core1( TaskHandle_t_core1 const pxMutexHolder_core1,
                                              UBaseType_t_core1 uxHighestPriorityWaitingTask_core1 )
    {
        TCB_t_core1 * const pxTCB_core1 = pxMutexHolder_core1;
        UBaseType_t_core1 uxPriorityUsedOnEntry_core1, uxPriorityToUse;
        const UBaseType_t_core1 uxOnlyOneMutexHeld = ( UBaseType_t_core1 ) 1;

        if( pxMutexHolder_core1 != NULL )
        {
            /* If pxMutexHolder_core1 is not NULL then the holder must hold at least
             * one mutex. */
            configASSERT_core1( pxTCB_core1->uxMutexesHeld_core1 );

            /* Determine the priority to which the priority of the task_core1 that
             * holds the mutex should be set.  This will be the greater of the
             * holding task_core1's base priority and the priority of the highest
             * priority task_core1 that is waiting to obtain the mutex. */
            if( pxTCB_core1->uxBasePriority_core1 < uxHighestPriorityWaitingTask_core1 )
            {
                uxPriorityToUse = uxHighestPriorityWaitingTask_core1;
            }
            else
            {
                uxPriorityToUse = pxTCB_core1->uxBasePriority_core1;
            }

            /* Does the priority need to change? */
            if( pxTCB_core1->uxPriority_core1 != uxPriorityToUse )
            {
                /* Only disinherit if no other mutexes are held.  This is a
                 * simplification in the priority inheritance implementation.  If
                 * the task_core1 that holds the mutex is also holding other mutexes then
                 * the other mutexes may have caused the priority inheritance. */
                if( pxTCB_core1->uxMutexesHeld_core1 == uxOnlyOneMutexHeld )
                {
                    /* If a task_core1 has timed out because it already holds the
                     * mutex it was trying to obtain then it cannot of inherited
                     * its own priority. */
                    configASSERT_core1( pxTCB_core1 != pxCurrentTCB_core1 );

                    /* Disinherit the priority, remembering the previous
                     * priority to facilitate determining the subject task_core1's
                     * state. */
                    traceTASK_PRIORITY_DISINHERIT_core1( pxTCB_core1, uxPriorityToUse );
                    uxPriorityUsedOnEntry_core1 = pxTCB_core1->uxPriority_core1;
                    pxTCB_core1->uxPriority_core1 = uxPriorityToUse;

                    /* Only reset the event list item value if the value is not
                     * being used for anything else. */
                    if( ( listGET_LIST_ITEM_VALUE_core1( &( pxTCB_core1->xEventListItem_core1 ) ) & taskEVENT_LIST_ITEM_VALUE_IN_USE_core1 ) == 0UL )
                    {
                        listSET_LIST_ITEM_VALUE_core1( &( pxTCB_core1->xEventListItem_core1 ), ( TickType_t_core1 ) configMAX_PRIORITIES_core1 - ( TickType_t_core1 ) uxPriorityToUse ); /*lint !e961 MISRA exception as the casts are only redundant for some ports. */
                    }
                    else
                    {
                        mtCOVERAGE_TEST_MARKER_core1();
                    }

                    /* If the running task_core1 is not the task_core1 that holds the mutex
                     * then the task_core1 that holds the mutex could be in either the
                     * Ready, Blocked or Suspended states.  Only remove the task_core1
                     * from its current state list if it is in the Ready state as
                     * the task_core1's priority is going to change and there is one
                     * Ready list per priority. */
                    if( listIS_CONTAINED_WITHIN_core1( &( pxReadyTasksLists_core1[ uxPriorityUsedOnEntry_core1 ] ), &( pxTCB_core1->xStateListItem_core1 ) ) != pdFALSE_core1 )
                    {
                        if( uxListRemove_core1( &( pxTCB_core1->xStateListItem_core1 ) ) == ( UBaseType_t_core1 ) 0 )
                        {
                            /* It is known that the task_core1 is in its ready list so
                             * there is no need to check again and the port level
                             * reset macro can be called directly. */
                            portRESET_READY_PRIORITY_core1( pxTCB_core1->uxPriority_core1, uxTopReadyPriority_core1 );
                        }
                        else
                        {
                            mtCOVERAGE_TEST_MARKER_core1();
                        }

                        prvAddTaskToReadyList_core1( pxTCB_core1 );
                    }
                    else
                    {
                        mtCOVERAGE_TEST_MARKER_core1();
                    }
                }
                else
                {
                    mtCOVERAGE_TEST_MARKER_core1();
                }
            }
            else
            {
                mtCOVERAGE_TEST_MARKER_core1();
            }
        }
        else
        {
            mtCOVERAGE_TEST_MARKER_core1();
        }
    }

#endif /* configUSE_MUTEXES_core1 */
