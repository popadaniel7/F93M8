    #endif

    #if ( configGENERATE_RUN_TIME_STATS_core1 == 1 )
        configRUN_TIME_COUNTER_TYPE_core1 ulRunTimeCounter_core1; /*< Stores the amount of time the task_core1 has spent in the Running state. */
    #endif

    #if ( ( configUSE_NEWLIB_REENTRANT_core1 == 1 ) || ( configUSE_C_RUNTIME_TLS_SUPPORT_core1 == 1 ) )
        configTLS_BLOCK_TYPE_core1 xTLSBlock_core1; /*< Memory block used as Thread Local Storage (TLS) Block for the task_core1. */
    #endif

    #if ( configUSE_TASK_NOTIFICATIONS_core1 == 1 )
        volatile uint32_t ulNotifiedValue_core1[ configTASK_NOTIFICATION_ARRAY_ENTRIES_core1 ];
        volatile uint8_t ucNotifyState_core1[ configTASK_NOTIFICATION_ARRAY_ENTRIES_core1 ];
    #endif

    /* See the comments in FreeRTOS_core1.h with the definition of
     * tskSTATIC_AND_DYNAMIC_ALLOCATION_POSSIBLE_core1. */
    #if ( tskSTATIC_AND_DYNAMIC_ALLOCATION_POSSIBLE_core1 != 0 ) /*lint !e731 !e9029 Macro has been consolidated for readability reasons. */
        uint8_t ucStaticallyAllocated_core1;                     /*< Set to pdTRUE_core1 if the task_core1 is a statically allocated to ensure no attempt is made to free the memory. */
    #endif

    #if ( INCLUDE_xTaskAbortDelay_core1 == 1 )
        uint8_t ucDelayAborted_core1;
    #endif

    #if ( configUSE_POSIX_ERRNO_core1 == 1 )
        int iTaskErrno_core1;
    #endif
