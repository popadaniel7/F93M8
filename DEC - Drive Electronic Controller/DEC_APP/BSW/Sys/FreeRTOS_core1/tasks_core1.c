#if ( INCLUDE_xTaskDelayUntil_core1 == 1 )

    BaseType_t_core1 xTaskDelayUntil_core1( TickType_t_core1 * const pxPreviousWakeTime_core1,
                                const TickType_t_core1 xTimeIncrement_core1 )
    {
        TickType_t_core1 xTimeToWake_core1;
        BaseType_t_core1 xAlreadyYielded_core1, xShouldDelay_core1 = pdFALSE_core1;

        configASSERT_core1( pxPreviousWakeTime_core1 );
        configASSERT_core1( ( xTimeIncrement_core1 > 0U ) );
        configASSERT_core1( uxSchedulerSuspended_core1 == 0 );

        vTaskSuspendAll_core1();
        {
            /* Minor optimisation.  The tick count cannot change in this
             * block. */
            const TickType_t_core1 xConstTickCount_core1 = xTickCount_core1;

            /* Generate the tick time at which the task_core1 wants to wake. */
            xTimeToWake_core1 = *pxPreviousWakeTime_core1 + xTimeIncrement_core1;

            if( xConstTickCount_core1 < *pxPreviousWakeTime_core1 )
            {
                /* The tick count has overflowed since this function was
                 * lasted called.  In this case the only time we should ever
                 * actually delay is if the wake time has also  overflowed,
                 * and the wake time is greater than the tick time.  When this
                 * is the case it is as if neither time had overflowed. */
                if( ( xTimeToWake_core1 < *pxPreviousWakeTime_core1 ) && ( xTimeToWake_core1 > xConstTickCount_core1 ) )
                {
                    xShouldDelay_core1 = pdTRUE_core1;
                }
                else
                {
                    mtCOVERAGE_TEST_MARKER_core1();
                }
            }
            else
            {
                /* The tick time has not overflowed.  In this case we will
                 * delay if either the wake time has overflowed, and/or the
                 * tick time is less than the wake time. */
                if( ( xTimeToWake_core1 < *pxPreviousWakeTime_core1 ) || ( xTimeToWake_core1 > xConstTickCount_core1 ) )
                {
                    xShouldDelay_core1 = pdTRUE_core1;
                }
                else
                {
                    mtCOVERAGE_TEST_MARKER_core1();
                }
            }

            /* Update the wake time ready for the next call. */
            *pxPreviousWakeTime_core1 = xTimeToWake_core1;

            if( xShouldDelay_core1 != pdFALSE_core1 )
            {
                traceTASK_DELAY_UNTIL_core1( xTimeToWake_core1 );

                /* prvAddCurrentTaskToDelayedList_core1() needs the block time, not
                 * the time to wake, so subtract the current tick count. */
                prvAddCurrentTaskToDelayedList_core1( xTimeToWake_core1 - xConstTickCount_core1, pdFALSE_core1 );
            }
            else
            {
                mtCOVERAGE_TEST_MARKER_core1();
            }
        }
        xAlreadyYielded_core1 = xTaskResumeAll_core1();

        /* Force a reschedule if xTaskResumeAll_core1 has not already done so, we may
         * have put ourselves to sleep. */
        if( xAlreadyYielded_core1 == pdFALSE_core1 )
        {
            portYIELD_WITHIN_API_core1();
        }
        else
        {
            mtCOVERAGE_TEST_MARKER_core1();
        }

        return xShouldDelay_core1;
    }

#endif /* INCLUDE_xTaskDelayUntil_core1 */
