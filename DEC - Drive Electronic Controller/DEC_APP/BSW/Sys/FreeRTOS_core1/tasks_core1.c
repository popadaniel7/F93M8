void vTaskPlaceOnEventList_core1( List_t_core1 * const pxEventList_core1,
                            const TickType_t_core1 xTicksToWait_core1 )
{
    configASSERT_core1( pxEventList_core1 );

    /* THIS FUNCTION MUST BE CALLED WITH EITHER INTERRUPTS DISABLED OR THE
     * SCHEDULER SUSPENDED AND THE QUEUE BEING ACCESSED LOCKED. */

    /* Place the event list item of the TCB in the appropriate event list.
     * This is placed in the list in priority order so the highest priority task_core1
     * is the first to be woken by the event.
     *
     * Note: Lists are sorted in ascending order by ListItem_t_core1.xItemValue_core1.
     * Normally, the xItemValue_core1 of a TCB's ListItem_t_core1 members is:
     *      xItemValue_core1 = ( configMAX_PRIORITIES_core1 - uxPriority_core1 )
     * Therefore, the event list is sorted in descending priority order.
     *
     * The queue that contains the event list is locked, preventing
     * simultaneous access from interrupts. */
    vListInsert_core1( pxEventList_core1, &( pxCurrentTCB_core1->xEventListItem_core1 ) );

    prvAddCurrentTaskToDelayedList_core1( xTicksToWait_core1, pdTRUE_core1 );
}
