#if ( ( INCLUDE_xTaskResumeFromISR_core1 == 1 ) && ( INCLUDE_vTaskSuspend_core1 == 1 ) )

    BaseType_t_core1 xTaskResumeFromISR_core1( TaskHandle_t_core1 xTaskToResume_core1 )
    {
        BaseType_t_core1 xYieldRequired_core1 = pdFALSE_core1;
        TCB_t_core1 * const pxTCB_core1 = xTaskToResume_core1;
        UBaseType_t_core1 uxSavedInterruptStatus_core1;

        configASSERT_core1( xTaskToResume_core1 );

        /* RTOS ports that support interrupt nesting have the concept of a
         * maximum  system call (or maximum API call) interrupt priority.
         * Interrupts that are  above the maximum system call priority are keep
         * permanently enabled, even when the RTOS kernel is in a critical section,
         * but cannot make any calls to FreeRTOS_core1 API functions.  If configASSERT_core1()
         * is defined in FreeRTOSConfig.h then
         * portASSERT_IF_INTERRUPT_PRIORITY_INVALID_core1() will result in an assertion
         * failure if a FreeRTOS_core1 API function is called from an interrupt that has
         * been assigned a priority above the configured maximum system call
         * priority.  Only FreeRTOS_core1 functions that end in FromISR can be called
         * from interrupts  that have been assigned a priority at or (logically)
         * below the maximum system call interrupt priority.  FreeRTOS_core1 maintains a
         * separate interrupt safe API to ensure interrupt entry is as fast and as
         * simple as possible.  More information (albeit Cortex-M specific) is
         * provided on the following link:
         * https://www.FreeRTOS_core1.org/RTOS-Cortex-M3-M4.html */
        portASSERT_IF_INTERRUPT_PRIORITY_INVALID_core1();

        uxSavedInterruptStatus_core1 = portSET_INTERRUPT_MASK_FROM_ISR_core1();
        {
            if( prvTaskIsTaskSuspended_core1( pxTCB_core1 ) != pdFALSE_core1 )
            {
                traceTASK_RESUME_FROM_ISR_core1( pxTCB_core1 );

                /* Check the ready lists can be accessed. */
                if( uxSchedulerSuspended_core1 == ( UBaseType_t_core1 ) pdFALSE_core1 )
                {
                    /* Ready lists can be accessed so move the task_core1 from the
                     * suspended list to the ready list directly. */
                    if( pxTCB_core1->uxPriority_core1 >= pxCurrentTCB_core1->uxPriority_core1 )
                    {
                        xYieldRequired_core1 = pdTRUE_core1;

                        /* Mark that a yield is pending in case the user is not
                         * using the return value to initiate a context switch
                         * from the ISR using portYIELD_FROM_ISR_core1. */
                        xYieldPending_core1 = pdTRUE_core1;
                    }
                    else
                    {
                        mtCOVERAGE_TEST_MARKER_core1();
                    }

                    ( void ) uxListRemove_core1( &( pxTCB_core1->xStateListItem_core1 ) );
                    prvAddTaskToReadyList_core1( pxTCB_core1 );
                }
                else
                {
                    /* The delayed or ready lists cannot be accessed so the task_core1
                     * is held in the pending ready list until the scheduler is
                     * unsuspended. */
                    vListInsertEnd_core1( &( xPendingReadyList_core1 ), &( pxTCB_core1->xEventListItem_core1 ) );
                }
            }
            else
            {
                mtCOVERAGE_TEST_MARKER_core1();
            }
        }
        portCLEAR_INTERRUPT_MASK_FROM_ISR_core1( uxSavedInterruptStatus_core1 );

        return xYieldRequired_core1;
    }

#endif /* ( ( INCLUDE_xTaskResumeFromISR_core1 == 1 ) && ( INCLUDE_vTaskSuspend_core1 == 1 ) ) */
