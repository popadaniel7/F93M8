#if ( INCLUDE_vTaskSuspend_core1 == 1 )

    void vTaskSuspend_core1( TaskHandle_t_core1 xTaskToSuspend_core1 )
    {
        TCB_t_core1 * pxTCB_core1;

        taskENTER_CRITICAL_core1();
        {
            /* If null is passed in here then it is the running task_core1 that is
             * being suspended. */
            pxTCB_core1 = prvGetTCBFromHandle_core1( xTaskToSuspend_core1 );

            traceTASK_SUSPEND_core1( pxTCB_core1 );

            /* Remove task_core1 from the ready/delayed list and place in the
             * suspended list. */
            if( uxListRemove_core1( &( pxTCB_core1->xStateListItem_core1 ) ) == ( UBaseType_t_core1 ) 0 )
            {
                taskRESET_READY_PRIORITY_core1( pxTCB_core1->uxPriority_core1 );
            }
            else
            {
                mtCOVERAGE_TEST_MARKER_core1();
            }

            /* Is the task_core1 waiting on an event also? */
            if( listLIST_ITEM_CONTAINER_core1( &( pxTCB_core1->xEventListItem_core1 ) ) != NULL )
            {
                ( void ) uxListRemove_core1( &( pxTCB_core1->xEventListItem_core1 ) );
            }
            else
            {
                mtCOVERAGE_TEST_MARKER_core1();
            }

            vListInsertEnd_core1( &xSuspendedTaskList_core1, &( pxTCB_core1->xStateListItem_core1 ) );

            #if ( configUSE_TASK_NOTIFICATIONS_core1 == 1 )
            {
                BaseType_t_core1 x;

                for( x = 0; x < configTASK_NOTIFICATION_ARRAY_ENTRIES_core1; x++ )
                {
                    if( pxTCB_core1->ucNotifyState_core1[ x ] == taskWAITING_NOTIFICATION_core1 )
                    {
                        /* The task_core1 was blocked to wait for a notification, but is
                         * now suspended, so no notification was received. */
                        pxTCB_core1->ucNotifyState_core1[ x ] = taskNOT_WAITING_NOTIFICATION_core1;
                    }
                }
            }
            #endif /* if ( configUSE_TASK_NOTIFICATIONS_core1 == 1 ) */
        }
        taskEXIT_CRITICAL_core1();

        if( xSchedulerRunning_core1 != pdFALSE_core1 )
        {
            /* Reset the next expected unblock time in case it referred to the
             * task_core1 that is now in the Suspended state. */
            taskENTER_CRITICAL_core1();
            {
                prvResetNextTaskUnblockTime_core1();
            }
            taskEXIT_CRITICAL_core1();
        }
        else
        {
            mtCOVERAGE_TEST_MARKER_core1();
        }

        if( pxTCB_core1 == pxCurrentTCB_core1 )
        {
            if( xSchedulerRunning_core1 != pdFALSE_core1 )
            {
                /* The current task_core1 has just been suspended. */
                configASSERT_core1( uxSchedulerSuspended_core1 == 0 );
                portYIELD_WITHIN_API_core1();
            }
            else
            {
                /* The scheduler is not running, but the task_core1 that was pointed
                 * to by pxCurrentTCB_core1 has just been suspended and pxCurrentTCB_core1
                 * must be adjusted to point to a different task_core1. */
                if( listCURRENT_LIST_LENGTH_core1( &xSuspendedTaskList_core1 ) == uxCurrentNumberOfTasks_core1 ) /*lint !e931 Right has no side effect, just volatile. */
                {
                    /* No other tasks are ready, so set pxCurrentTCB_core1 back to
                     * NULL so when the next task_core1 is created pxCurrentTCB_core1 will
                     * be set to point to it no matter what its relative priority
                     * is. */
                    pxCurrentTCB_core1 = NULL;
                }
                else
                {
                    vTaskSwitchContext_core1();
                }
            }
        }
        else
        {
            mtCOVERAGE_TEST_MARKER_core1();
        }
    }

#endif /* INCLUDE_vTaskSuspend_core1 */
