BaseType_t_core1 xTaskRemoveFromEventList_core1( const List_t_core1 * const pxEventList_core1 )
{
    TCB_t_core1 * pxUnblockedTCB;
    BaseType_t_core1 xReturn_core1;

    /* THIS FUNCTION MUST BE CALLED FROM A CRITICAL SECTION.  It can also be
     * called from a critical section within an ISR. */

    /* The event list is sorted in priority order, so the first in the list can
     * be removed as it is known to be the highest priority.  Remove the TCB from
     * the delayed list, and add it to the ready list.
     *
     * If an event is for a queue that is locked then this function will never
     * get called - the lock count on the queue will get modified instead.  This
     * means exclusive access to the event list is guaranteed here.
     *
     * This function assumes that a check has already been made to ensure that
     * pxEventList_core1 is not empty. */
    pxUnblockedTCB = listGET_OWNER_OF_HEAD_ENTRY_core1( pxEventList_core1 ); /*lint !e9079 void * is used as this macro is used with timers_core1 and co-routines too.  Alignment is known to be fine as the type of the pointer stored and retrieved is the same. */
    configASSERT_core1( pxUnblockedTCB );
    listREMOVE_ITEM_core1( &( pxUnblockedTCB->xEventListItem_core1 ) );

    if( uxSchedulerSuspended_core1 == ( UBaseType_t_core1 ) pdFALSE_core1 )
    {
        listREMOVE_ITEM_core1( &( pxUnblockedTCB->xStateListItem_core1 ) );
        prvAddTaskToReadyList_core1( pxUnblockedTCB );

        #if ( configUSE_TICKLESS_IDLE_core1 != 0 )
        {
            /* If a task_core1 is blocked on a kernel object then xNextTaskUnblockTime_core1
             * might be set to the blocked task_core1's time out time.  If the task_core1 is
             * unblocked for a reason other than a timeout xNextTaskUnblockTime_core1 is
             * normally left unchanged, because it is automatically reset to a new
             * value when the tick count equals xNextTaskUnblockTime_core1.  However if
             * tickless idling is used it might be more important to enter sleep mode
             * at the earliest possible time - so reset xNextTaskUnblockTime_core1 here to
             * ensure it is updated at the earliest possible time. */
            prvResetNextTaskUnblockTime_core1();
        }
        #endif
    }
    else
    {
        /* The delayed and ready lists cannot be accessed, so hold this task_core1
         * pending until the scheduler is resumed. */
        listINSERT_END_core1( &( xPendingReadyList_core1 ), &( pxUnblockedTCB->xEventListItem_core1 ) );
    }

    if( pxUnblockedTCB->uxPriority_core1 > pxCurrentTCB_core1->uxPriority_core1 )
    {
        /* Return true if the task_core1 removed from the event list has a higher
         * priority than the calling task_core1.  This allows the calling task_core1 to know if
         * it should force a context switch now. */
        xReturn_core1 = pdTRUE_core1;

        /* Mark that a yield is pending in case the user is not using the
         * "xHigherPriorityTaskWoken_core1" parameter to an ISR safe FreeRTOS_core1 function. */
        xYieldPending_core1 = pdTRUE_core1;
    }
    else
    {
        xReturn_core1 = pdFALSE_core1;
    }

    return xReturn_core1;
}
