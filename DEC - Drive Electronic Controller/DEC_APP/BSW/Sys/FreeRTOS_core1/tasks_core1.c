#if ( configUSE_TICKLESS_IDLE_core1 != 0 )

    static TickType_t_core1 prvGetExpectedIdleTime_core1( void )
    {
        TickType_t_core1 xReturn_core1;
        UBaseType_t_core1 uxHigherPriorityReadyTasks_core1 = pdFALSE_core1;

        /* uxHigherPriorityReadyTasks_core1 takes care of the case where
         * configUSE_PREEMPTION_core1 is 0, so there may be tasks above the idle priority
         * task_core1 that are in the Ready state, even though the idle task_core1 is
         * running. */
        #if ( configUSE_PORT_OPTIMISED_TASK_SELECTION_core1 == 0 )
        {
            if( uxTopReadyPriority_core1 > tskIDLE_PRIORITY_core1 )
            {
                uxHigherPriorityReadyTasks_core1 = pdTRUE_core1;
            }
        }
        #else
        {
            const UBaseType_t_core1 uxLeastSignificantBit_core1 = ( UBaseType_t_core1 ) 0x01;

            /* When port optimised task_core1 selection is used the uxTopReadyPriority_core1
             * variable is used as a bit map.  If bits other than the least
             * significant bit are set then there are tasks that have a priority
             * above the idle priority that are in the Ready state.  This takes
             * care of the case where the co-operative scheduler is in use. */
            if( uxTopReadyPriority_core1 > uxLeastSignificantBit_core1 )
            {
                uxHigherPriorityReadyTasks_core1 = pdTRUE_core1;
            }
        }
        #endif /* if ( configUSE_PORT_OPTIMISED_TASK_SELECTION_core1 == 0 ) */

        if( pxCurrentTCB_core1->uxPriority_core1 > tskIDLE_PRIORITY_core1 )
        {
            xReturn_core1 = 0;
        }
        else if( listCURRENT_LIST_LENGTH_core1( &( pxReadyTasksLists_core1[ tskIDLE_PRIORITY_core1 ] ) ) > 1 )
        {
            /* There are other idle priority tasks in the ready state.  If
             * time slicing is used then the very next tick interrupt must be
             * processed. */
            xReturn_core1 = 0;
        }
        else if( uxHigherPriorityReadyTasks_core1 != pdFALSE_core1 )
        {
            /* There are tasks in the Ready state that have a priority above the
             * idle priority.  This path can only be reached if
             * configUSE_PREEMPTION_core1 is 0. */
            xReturn_core1 = 0;
        }
        else
        {
            xReturn_core1 = xNextTaskUnblockTime_core1 - xTickCount_core1;
        }

        return xReturn_core1;
    }

#endif /* configUSE_TICKLESS_IDLE_core1 */
