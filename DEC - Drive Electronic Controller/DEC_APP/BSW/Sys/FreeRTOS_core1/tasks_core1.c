void vTaskSwitchContext_core1( void )
{
    if( uxSchedulerSuspended_core1 != ( UBaseType_t_core1 ) pdFALSE_core1 )
    {
        /* The scheduler is currently suspended - do not allow a context
         * switch. */
        xYieldPending_core1 = pdTRUE_core1;
    }
    else
    {
        xYieldPending_core1 = pdFALSE_core1;
        traceTASK_SWITCHED_OUT_core1();

        #if ( configGENERATE_RUN_TIME_STATS_core1 == 1 )
        {
            #ifdef portALT_GET_RUN_TIME_COUNTER_VALUE_core1
                portALT_GET_RUN_TIME_COUNTER_VALUE_core1( ulTotalRunTime_core1 );
            #else
                ulTotalRunTime_core1 = portGET_RUN_TIME_COUNTER_VALUE_core1();
            #endif

            /* Add the amount of time the task_core1 has been running to the
             * accumulated time so far.  The time the task_core1 started running was
             * stored in ulTaskSwitchedInTime_core1.  Note that there is no overflow
             * protection here so count values are only valid until the timer
             * overflows.  The guard against negative values is to protect
             * against suspect run time stat counter implementations - which
             * are provided by the application, not the kernel. */
            if( ulTotalRunTime_core1 > ulTaskSwitchedInTime_core1 )
            {
                pxCurrentTCB_core1->ulRunTimeCounter_core1 += ( ulTotalRunTime_core1 - ulTaskSwitchedInTime_core1 );
            }
            else
            {
                mtCOVERAGE_TEST_MARKER_core1();
            }

            ulTaskSwitchedInTime_core1 = ulTotalRunTime_core1;
        }
        #endif /* configGENERATE_RUN_TIME_STATS_core1 */

        /* Check for stack overflow, if configured. */
        taskCHECK_FOR_STACK_OVERFLOW();

        /* Before the currently running task_core1 is switched out, save its errno. */
        #if ( configUSE_POSIX_ERRNO_core1 == 1 )
        {
            pxCurrentTCB_core1->iTaskErrno_core1 = FreeRTOS_errno_core1;
        }
        #endif

        /* Select a new task_core1 to run using either the generic C or port
         * optimised asm code. */
        taskSELECT_HIGHEST_PRIORITY_TASK_core1(); /*lint !e9079 void * is used as this macro is used with timers_core1 and co-routines too.  Alignment is known to be fine as the type of the pointer stored and retrieved is the same. */
        traceTASK_SWITCHED_IN_core1();

        /* After the new task_core1 is switched in, update the global errno. */
        #if ( configUSE_POSIX_ERRNO_core1 == 1 )
        {
            FreeRTOS_errno_core1 = pxCurrentTCB_core1->iTaskErrno_core1;
        }
        #endif

        #if ( ( configUSE_NEWLIB_REENTRANT_core1 == 1 ) || ( configUSE_C_RUNTIME_TLS_SUPPORT_core1 == 1 ) )
        {
            /* Switch C-Runtime's TLS Block to point to the TLS
             * Block specific to this task_core1. */
            configSET_TLS_BLOCK_core1( pxCurrentTCB_core1->xTLSBlock_core1 );
        }
        #endif
    }
}
