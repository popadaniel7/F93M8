static void prvAddNewTaskToReadyList_core1( TCB_t_core1 * pxNewTCB_core1 )
{
    /* Ensure interrupts don't access the task_core1 lists while the lists are being
     * updated. */
    taskENTER_CRITICAL_core1();
    {
        uxCurrentNumberOfTasks_core1++;

        if( pxCurrentTCB_core1 == NULL )
        {
            /* There are no other tasks, or all the other tasks are in
             * the suspended state - make this the current task_core1. */
            pxCurrentTCB_core1 = pxNewTCB_core1;

            if( uxCurrentNumberOfTasks_core1 == ( UBaseType_t_core1 ) 1 )
            {
                /* This is the first task_core1 to be created so do the preliminary
                 * initialisation required.  We will not recover if this call
                 * fails, but we will report the failure. */
                prvInitialiseTaskLists_core1();
            }
            else
            {
                mtCOVERAGE_TEST_MARKER_core1();
            }
        }
        else
        {
            /* If the scheduler is not already running, make this task_core1 the
             * current task_core1 if it is the highest priority task_core1 to be created
             * so far. */
            if( xSchedulerRunning_core1 == pdFALSE_core1 )
            {
                if( pxCurrentTCB_core1->uxPriority_core1 <= pxNewTCB_core1->uxPriority_core1 )
                {
                    pxCurrentTCB_core1 = pxNewTCB_core1;
                }
                else
                {
                    mtCOVERAGE_TEST_MARKER_core1();
                }
            }
            else
            {
                mtCOVERAGE_TEST_MARKER_core1();
            }
        }

        uxTaskNumber_core1++;

        #if ( configUSE_TRACE_FACILITY_core1 == 1 )
        {
            /* Add a counter into the TCB for tracing only. */
            pxNewTCB_core1->uxTCBNumber_core1 = uxTaskNumber_core1;
        }
        #endif /* configUSE_TRACE_FACILITY_core1 */
        traceTASK_CREATE_core1( pxNewTCB_core1 );

        prvAddTaskToReadyList_core1( pxNewTCB_core1 );

        portSETUP_TCB_core1( pxNewTCB_core1 );
    }
    taskEXIT_CRITICAL_core1();

    if( xSchedulerRunning_core1 != pdFALSE_core1 )
    {
        /* If the created task_core1 is of a higher priority than the current task_core1
         * then it should run now. */
        if( pxCurrentTCB_core1->uxPriority_core1 < pxNewTCB_core1->uxPriority_core1 )
        {
            taskYIELD_IF_USING_PREEMPTION_core1();
        }
        else
        {
            mtCOVERAGE_TEST_MARKER_core1();
        }
    }
    else
    {
        mtCOVERAGE_TEST_MARKER_core1();
    }
}
