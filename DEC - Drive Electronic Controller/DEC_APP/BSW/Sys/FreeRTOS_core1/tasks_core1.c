#if ( INCLUDE_vTaskSuspend_core1 == 1 )

    static BaseType_t_core1 prvTaskIsTaskSuspended_core1( const TaskHandle_t_core1 xTask_core1 )
    {
        BaseType_t_core1 xReturn_core1 = pdFALSE_core1;
        const TCB_t_core1 * const pxTCB_core1 = xTask_core1;

        /* Accesses xPendingReadyList_core1 so must be called from a critical
         * section. */

        /* It does not make sense to check if the calling task_core1 is suspended. */
        configASSERT_core1( xTask_core1 );

        /* Is the task_core1 being resumed actually in the suspended list? */
        if( listIS_CONTAINED_WITHIN_core1( &xSuspendedTaskList_core1, &( pxTCB_core1->xStateListItem_core1 ) ) != pdFALSE_core1 )
        {
            /* Has the task_core1 already been resumed from within an ISR? */
            if( listIS_CONTAINED_WITHIN_core1( &xPendingReadyList_core1, &( pxTCB_core1->xEventListItem_core1 ) ) == pdFALSE_core1 )
            {
                /* Is it in the suspended list because it is in the Suspended
                 * state, or because is is blocked with no timeout? */
                if( listIS_CONTAINED_WITHIN_core1( NULL, &( pxTCB_core1->xEventListItem_core1 ) ) != pdFALSE_core1 ) /*lint !e961.  The cast is only redundant when NULL is used. */
                {
                    xReturn_core1 = pdTRUE_core1;
                }
                else
                {
                    mtCOVERAGE_TEST_MARKER_core1();
                }
            }
            else
            {
                mtCOVERAGE_TEST_MARKER_core1();
            }
        }
        else
        {
            mtCOVERAGE_TEST_MARKER_core1();
        }

        return xReturn_core1;
    } /*lint !e818 xTask_core1 cannot be a pointer to const because it is a typedef. */

#endif /* INCLUDE_vTaskSuspend_core1 */
