static void prvInitialiseNewTask_core1( TaskFunction_t_core1 pxTaskCode_core1,
                                  const char * const pcName_core1, /*lint !e971 Unqualified char types are allowed for strings and single characters only. */
                                  const uint32_t ulStackDepth_core1,
                                  void * const pvParameters_core1,
                                  UBaseType_t_core1 uxPriority_core1,
                                  TaskHandle_t_core1 * const pxCreatedTask_core1,
                                  TCB_t_core1 * pxNewTCB_core1,
                                  const MemoryRegion_t_core1 * const xRegions_core1 )
{
    StackType_t_core1 * pxTopOfStack_core1;
    UBaseType_t_core1 x;

    #if ( portUSING_MPU_WRAPPERS_core1 == 1 )
        /* Should the task_core1 be created in privileged mode? */
        BaseType_t_core1 xRunPrivileged_core1;

        if( ( uxPriority_core1 & portPRIVILEGE_BIT_core1 ) != 0U )
        {
            xRunPrivileged_core1 = pdTRUE_core1;
        }
        else
        {
            xRunPrivileged_core1 = pdFALSE_core1;
        }
        uxPriority_core1 &= ~portPRIVILEGE_BIT_core1;
    #endif /* portUSING_MPU_WRAPPERS_core1 == 1 */

    /* Avoid dependency on memset() if it is not required. */
    #if ( tskSET_NEW_STACKS_TO_KNOWN_VALUE_core1 == 1 )
    {
        /* Fill the stack with a known value to assist debugging. */
        ( void ) memset( pxNewTCB_core1->pxStack_core1, ( int ) tskSTACK_FILL_BYTE_core1, ( size_t ) ulStackDepth_core1 * sizeof( StackType_t_core1 ) );
    }
    #endif /* tskSET_NEW_STACKS_TO_KNOWN_VALUE_core1 */

    /* Calculate the top of stack address.  This depends on whether the stack
     * grows from high memory to low (as per the 80x86) or vice versa.
     * portSTACK_GROWTH_core1 is used to make the result positive or negative as required
     * by the port. */
    #if ( portSTACK_GROWTH_core1 < 0 )
    {
        pxTopOfStack_core1 = &( pxNewTCB_core1->pxStack_core1[ ulStackDepth_core1 - ( uint32_t ) 1 ] );
        pxTopOfStack_core1 = ( StackType_t_core1 * ) ( ( ( portPOINTER_SIZE_TYPE_core1 ) pxTopOfStack_core1 ) & ( ~( ( portPOINTER_SIZE_TYPE_core1 ) portBYTE_ALIGNMENT_MASK_core1 ) ) ); /*lint !e923 !e9033 !e9078 MISRA exception.  Avoiding casts between pointers and integers is not practical.  Size differences accounted for using portPOINTER_SIZE_TYPE_core1 type.  Checked by assert(). */

        /* Check the alignment of the calculated top of stack is correct. */
        configASSERT_core1( ( ( ( portPOINTER_SIZE_TYPE_core1 ) pxTopOfStack_core1 & ( portPOINTER_SIZE_TYPE_core1 ) portBYTE_ALIGNMENT_MASK_core1 ) == 0UL ) );

        #if ( configRECORD_STACK_HIGH_ADDRESS_core1 == 1 )
        {
            /* Also record the stack's high address, which may assist
             * debugging. */
            pxNewTCB_core1->pxEndOfStack_core1 = pxTopOfStack_core1;
        }
        #endif /* configRECORD_STACK_HIGH_ADDRESS_core1 */
    }
    #else /* portSTACK_GROWTH_core1 */
    {
        pxTopOfStack_core1 = pxNewTCB_core1->pxStack_core1;

        /* Check the alignment of the stack buffer is correct. */
        configASSERT_core1( ( ( ( portPOINTER_SIZE_TYPE_core1 ) pxNewTCB_core1->pxStack_core1 & ( portPOINTER_SIZE_TYPE_core1 ) portBYTE_ALIGNMENT_MASK_core1 ) == 0UL ) );

        /* The other extreme of the stack space is required if stack checking is
         * performed. */
        pxNewTCB_core1->pxEndOfStack_core1 = pxNewTCB_core1->pxStack_core1 + ( ulStackDepth_core1 - ( uint32_t ) 1 );
    }
    #endif /* portSTACK_GROWTH_core1 */

    /* Store the task_core1 name in the TCB. */
    if( pcName_core1 != NULL )
    {
        for( x = ( UBaseType_t_core1 ) 0; x < ( UBaseType_t_core1 ) configMAX_TASK_NAME_LEN_core1; x++ )
        {
            pxNewTCB_core1->pcTaskName_core1[ x ] = pcName_core1[ x ];

            /* Don't copy all configMAX_TASK_NAME_LEN_core1 if the string is shorter than
             * configMAX_TASK_NAME_LEN_core1 characters just in case the memory after the
             * string is not accessible (extremely unlikely). */
            if( pcName_core1[ x ] == ( char ) 0x00 )
            {
                break;
            }
            else
            {
                mtCOVERAGE_TEST_MARKER_core1();
            }
        }

        /* Ensure the name string is terminated in the case that the string length
         * was greater or equal to configMAX_TASK_NAME_LEN_core1. */
        pxNewTCB_core1->pcTaskName_core1[ configMAX_TASK_NAME_LEN_core1 - 1 ] = '\0';
    }
    else
    {
        mtCOVERAGE_TEST_MARKER_core1();
    }

    /* This is used as an array index so must ensure it's not too large. */
    configASSERT_core1( uxPriority_core1 < configMAX_PRIORITIES_core1 );

    if( uxPriority_core1 >= ( UBaseType_t_core1 ) configMAX_PRIORITIES_core1 )
    {
        uxPriority_core1 = ( UBaseType_t_core1 ) configMAX_PRIORITIES_core1 - ( UBaseType_t_core1 ) 1U;
    }
    else
    {
        mtCOVERAGE_TEST_MARKER_core1();
    }

    pxNewTCB_core1->uxPriority_core1 = uxPriority_core1;
    #if ( configUSE_MUTEXES_core1 == 1 )
    {
        pxNewTCB_core1->uxBasePriority_core1 = uxPriority_core1;
    }
    #endif /* configUSE_MUTEXES_core1 */

    vListInitialiseItem_core1( &( pxNewTCB_core1->xStateListItem_core1 ) );
    vListInitialiseItem_core1( &( pxNewTCB_core1->xEventListItem_core1 ) );

    /* Set the pxNewTCB_core1 as a link back from the ListItem_t_core1.  This is so we can get
     * back to  the containing TCB from a generic item in a list. */
    listSET_LIST_ITEM_OWNER_core1( &( pxNewTCB_core1->xStateListItem_core1 ), pxNewTCB_core1 );

    /* Event lists are always in priority order. */
    listSET_LIST_ITEM_VALUE_core1( &( pxNewTCB_core1->xEventListItem_core1 ), ( TickType_t_core1 ) configMAX_PRIORITIES_core1 - ( TickType_t_core1 ) uxPriority_core1 ); /*lint !e961 MISRA exception as the casts are only redundant for some ports. */
    listSET_LIST_ITEM_OWNER_core1( &( pxNewTCB_core1->xEventListItem_core1 ), pxNewTCB_core1 );

    #if ( portUSING_MPU_WRAPPERS_core1 == 1 )
    {
        vPortStoreTaskMPUSettings_core1( &( pxNewTCB_core1->xMPUSettings_core1 ), xRegions_core1, pxNewTCB_core1->pxStack_core1, ulStackDepth_core1 );
    }
    #else
    {
        /* Avoid compiler warning about unreferenced parameter. */
        ( void ) xRegions_core1;
    }
    #endif

    #if ( ( configUSE_NEWLIB_REENTRANT_core1 == 1 ) || ( configUSE_C_RUNTIME_TLS_SUPPORT_core1 == 1 ) )
    {
        /* Allocate and initialize memory for the task_core1's TLS Block. */
        configINIT_TLS_BLOCK_core1( pxNewTCB_core1->xTLSBlock_core1 );
    }
    #endif

    /* Initialize the TCB stack to look as if the task_core1 was already running,
     * but had been interrupted by the scheduler.  The return address is set
     * to the start of the task_core1 function. Once the stack has been initialised
     * the top of stack variable is updated. */
    #if ( portUSING_MPU_WRAPPERS_core1 == 1 )
    {
        /* If the port has capability to detect stack overflow,
         * pass the stack end address to the stack initialization
         * function as well. */
        #if ( portHAS_STACK_OVERFLOW_CHECKING_core1 == 1 )
        {
            #if ( portSTACK_GROWTH_core1 < 0 )
            {
                pxNewTCB_core1->pxTopOfStack_core1 = pxPortInitialiseStack_core1( pxTopOfStack_core1, pxNewTCB_core1->pxStack_core1, pxTaskCode_core1, pvParameters_core1, xRunPrivileged_core1 );
            }
            #else /* portSTACK_GROWTH_core1 */
            {
                pxNewTCB_core1->pxTopOfStack_core1 = pxPortInitialiseStack_core1( pxTopOfStack_core1, pxNewTCB_core1->pxEndOfStack_core1, pxTaskCode_core1, pvParameters_core1, xRunPrivileged_core1 );
            }
            #endif /* portSTACK_GROWTH_core1 */
        }
        #else /* portHAS_STACK_OVERFLOW_CHECKING_core1 */
        {
            pxNewTCB_core1->pxTopOfStack_core1 = pxPortInitialiseStack_core1( pxTopOfStack_core1, pxTaskCode_core1, pvParameters_core1, xRunPrivileged_core1 );
        }
        #endif /* portHAS_STACK_OVERFLOW_CHECKING_core1 */
    }
    #else /* portUSING_MPU_WRAPPERS_core1 */
    {
        /* If the port has capability to detect stack overflow,
         * pass the stack end address to the stack initialization
         * function as well. */
        #if ( portHAS_STACK_OVERFLOW_CHECKING_core1 == 1 )
        {
            #if ( portSTACK_GROWTH_core1 < 0 )
            {
                pxNewTCB_core1->pxTopOfStack_core1 = pxPortInitialiseStack_core1( pxTopOfStack_core1, pxNewTCB_core1->pxStack_core1, pxTaskCode_core1, pvParameters_core1 );
            }
            #else /* portSTACK_GROWTH_core1 */
            {
                pxNewTCB_core1->pxTopOfStack_core1 = pxPortInitialiseStack_core1( pxTopOfStack_core1, pxNewTCB_core1->pxEndOfStack_core1, pxTaskCode_core1, pvParameters_core1 );
            }
            #endif /* portSTACK_GROWTH_core1 */
        }
        #else /* portHAS_STACK_OVERFLOW_CHECKING_core1 */
        {
            pxNewTCB_core1->pxTopOfStack_core1 = pxPortInitialiseStack_core1( pxTopOfStack_core1, pxTaskCode_core1, pvParameters_core1 );
        }
        #endif /* portHAS_STACK_OVERFLOW_CHECKING_core1 */
    }
    #endif /* portUSING_MPU_WRAPPERS_core1 */

    if( pxCreatedTask_core1 != NULL )
    {
        /* Pass the handle out in an anonymous way.  The handle can be used to
         * change the created task_core1's priority, delete the created task_core1, etc.*/
        *pxCreatedTask_core1 = ( TaskHandle_t_core1 ) pxNewTCB_core1;
    }
    else
    {
        mtCOVERAGE_TEST_MARKER_core1();
    }
}
