BaseType_t_core1 xTaskIncrementTick_core1( void )
{
    TCB_t_core1 * pxTCB_core1;
    TickType_t_core1 xItemValue_core1;
    BaseType_t_core1 xSwitchRequired = pdFALSE_core1;

    /* Called by the portable layer each time a tick interrupt occurs.
     * Increments the tick then checks to see if the new tick value will cause any
     * tasks to be unblocked. */
    traceTASK_INCREMENT_TICK_core1( xTickCount_core1 );

    if( uxSchedulerSuspended_core1 == ( UBaseType_t_core1 ) pdFALSE_core1 )
    {
        /* Minor optimisation.  The tick count cannot change in this
         * block. */
        const TickType_t_core1 xConstTickCount_core1 = xTickCount_core1 + ( TickType_t_core1 ) 1;

        /* Increment the RTOS tick, switching the delayed and overflowed
         * delayed lists if it wraps to 0. */
        xTickCount_core1 = xConstTickCount_core1;

        if( xConstTickCount_core1 == ( TickType_t_core1 ) 0U ) /*lint !e774 'if' does not always evaluate to false as it is looking for an overflow. */
        {
            taskSWITCH_DELAYED_LISTS_core1();
        }
        else
        {
            mtCOVERAGE_TEST_MARKER_core1();
        }

        /* See if this tick has made a timeout expire.  Tasks are stored in
         * the  queue in the order of their wake time - meaning once one task_core1
         * has been found whose block time has not expired there is no need to
         * look any further down the list. */
        if( xConstTickCount_core1 >= xNextTaskUnblockTime_core1 )
        {
            for( ; ; )
            {
                if( listLIST_IS_EMPTY_core1( pxDelayedTaskList_core1 ) != pdFALSE_core1 )
                {
                    /* The delayed list is empty.  Set xNextTaskUnblockTime_core1
                     * to the maximum possible value so it is extremely
                     * unlikely that the
                     * if( xTickCount_core1 >= xNextTaskUnblockTime_core1 ) test will pass
                     * next time through. */
                    xNextTaskUnblockTime_core1 = portMAX_DELAY_core1; /*lint !e961 MISRA exception as the casts are only redundant for some ports. */
                    break;
                }
                else
                {
                    /* The delayed list is not empty, get the value of the
                     * item at the head of the delayed list.  This is the time
                     * at which the task_core1 at the head of the delayed list must
                     * be removed from the Blocked state. */
                    pxTCB_core1 = listGET_OWNER_OF_HEAD_ENTRY_core1( pxDelayedTaskList_core1 ); /*lint !e9079 void * is used as this macro is used with timers_core1 and co-routines too.  Alignment is known to be fine as the type of the pointer stored and retrieved is the same. */
                    xItemValue_core1 = listGET_LIST_ITEM_VALUE_core1( &( pxTCB_core1->xStateListItem_core1 ) );

                    if( xConstTickCount_core1 < xItemValue_core1 )
                    {
                        /* It is not time to unblock this item yet, but the
                         * item value is the time at which the task_core1 at the head
                         * of the blocked list must be removed from the Blocked
                         * state -  so record the item value in
                         * xNextTaskUnblockTime_core1. */
                        xNextTaskUnblockTime_core1 = xItemValue_core1;
                        break; /*lint !e9011 Code structure here is deemed easier to understand with multiple breaks. */
                    }
                    else
                    {
                        mtCOVERAGE_TEST_MARKER_core1();
                    }

                    /* It is time to remove the item from the Blocked state. */
                    listREMOVE_ITEM_core1( &( pxTCB_core1->xStateListItem_core1 ) );

                    /* Is the task_core1 waiting on an event also?  If so remove
                     * it from the event list. */
                    if( listLIST_ITEM_CONTAINER_core1( &( pxTCB_core1->xEventListItem_core1 ) ) != NULL )
                    {
                        listREMOVE_ITEM_core1( &( pxTCB_core1->xEventListItem_core1 ) );
                    }
                    else
                    {
                        mtCOVERAGE_TEST_MARKER_core1();
                    }

                    /* Place the unblocked task_core1 into the appropriate ready
                     * list. */
                    prvAddTaskToReadyList_core1( pxTCB_core1 );

                    /* A task_core1 being unblocked cannot cause an immediate
                     * context switch if preemption is turned off. */
                    #if ( configUSE_PREEMPTION_core1 == 1 )
                    {
                        /* Preemption is on, but a context switch should
                         * only be performed if the unblocked task_core1's
                         * priority is higher than the currently executing
                         * task_core1.
                         * The case of equal priority tasks sharing
                         * processing time (which happens when both
                         * preemption and time slicing are on) is
                         * handled below.*/
                        if( pxTCB_core1->uxPriority_core1 > pxCurrentTCB_core1->uxPriority_core1 )
                        {
                            xSwitchRequired = pdTRUE_core1;
                        }
                        else
                        {
                            mtCOVERAGE_TEST_MARKER_core1();
                        }
                    }
                    #endif /* configUSE_PREEMPTION_core1 */
                }
            }
        }

        /* Tasks of equal priority to the currently running task_core1 will share
         * processing time (time slice) if preemption is on, and the application
         * writer has not explicitly turned time slicing off. */
        #if ( ( configUSE_PREEMPTION_core1 == 1 ) && ( configUSE_TIME_SLICING_core1 == 1 ) )
        {
            if( listCURRENT_LIST_LENGTH_core1( &( pxReadyTasksLists_core1[ pxCurrentTCB_core1->uxPriority_core1 ] ) ) > ( UBaseType_t_core1 ) 1 )
            {
                xSwitchRequired = pdTRUE_core1;
            }
            else
            {
                mtCOVERAGE_TEST_MARKER_core1();
            }
        }
        #endif /* ( ( configUSE_PREEMPTION_core1 == 1 ) && ( configUSE_TIME_SLICING_core1 == 1 ) ) */

        #if ( configUSE_TICK_HOOK_core1 == 1 )
        {
            /* Guard against the tick hook being called when the pended tick
             * count is being unwound (when the scheduler is being unlocked). */
            if( xPendedTicks_core1 == ( TickType_t_core1 ) 0 )
            {
                vApplicationTickHook_core1();
            }
            else
            {
                mtCOVERAGE_TEST_MARKER_core1();
            }
        }
        #endif /* configUSE_TICK_HOOK_core1 */

        #if ( configUSE_PREEMPTION_core1 == 1 )
        {
            if( xYieldPending_core1 != pdFALSE_core1 )
            {
                xSwitchRequired = pdTRUE_core1;
            }
            else
            {
                mtCOVERAGE_TEST_MARKER_core1();
            }
        }
        #endif /* configUSE_PREEMPTION_core1 */
    }
    else
    {
        ++xPendedTicks_core1;

        /* The tick hook gets called at regular intervals, even if the
         * scheduler is locked. */
        #if ( configUSE_TICK_HOOK_core1 == 1 )
        {
            vApplicationTickHook_core1();
        }
        #endif
    }

    return xSwitchRequired;
}
