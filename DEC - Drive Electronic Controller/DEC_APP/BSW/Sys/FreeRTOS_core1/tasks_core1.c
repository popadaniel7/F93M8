static void prvAddCurrentTaskToDelayedList_core1( TickType_t_core1 xTicksToWait_core1,
                                            const BaseType_t_core1 xCanBlockIndefinitely_core1 )
{
    TickType_t_core1 xTimeToWake_core1;
    const TickType_t_core1 xConstTickCount_core1 = xTickCount_core1;

    #if ( INCLUDE_xTaskAbortDelay_core1 == 1 )
    {
        /* About to enter a delayed list, so ensure the ucDelayAborted_core1 flag is
         * reset to pdFALSE_core1 so it can be detected as having been set to pdTRUE_core1
         * when the task_core1 leaves the Blocked state. */
        pxCurrentTCB_core1->ucDelayAborted_core1 = pdFALSE_core1;
    }
    #endif

    /* Remove the task_core1 from the ready list before adding it to the blocked list
     * as the same list item is used for both lists. */
    if( uxListRemove_core1( &( pxCurrentTCB_core1->xStateListItem_core1 ) ) == ( UBaseType_t_core1 ) 0 )
    {
        /* The current task_core1 must be in a ready list, so there is no need to
         * check, and the port reset macro can be called directly. */
        portRESET_READY_PRIORITY_core1( pxCurrentTCB_core1->uxPriority_core1, uxTopReadyPriority_core1 ); /*lint !e931 pxCurrentTCB_core1 cannot change as it is the calling task_core1.  pxCurrentTCB_core1->uxPriority_core1 and uxTopReadyPriority_core1 cannot change as called with scheduler suspended or in a critical section. */
    }
    else
    {
        mtCOVERAGE_TEST_MARKER_core1();
    }

    #if ( INCLUDE_vTaskSuspend_core1 == 1 )
    {
        if( ( xTicksToWait_core1 == portMAX_DELAY_core1 ) && ( xCanBlockIndefinitely_core1 != pdFALSE_core1 ) )
        {
            /* Add the task_core1 to the suspended task_core1 list instead of a delayed task_core1
             * list to ensure it is not woken by a timing event.  It will block
             * indefinitely. */
            listINSERT_END_core1( &xSuspendedTaskList_core1, &( pxCurrentTCB_core1->xStateListItem_core1 ) );
        }
        else
        {
            /* Calculate the time at which the task_core1 should be woken if the event
             * does not occur.  This may overflow but this doesn't matter, the
             * kernel will manage it correctly. */
            xTimeToWake_core1 = xConstTickCount_core1 + xTicksToWait_core1;

            /* The list item will be inserted in wake time order. */
            listSET_LIST_ITEM_VALUE_core1( &( pxCurrentTCB_core1->xStateListItem_core1 ), xTimeToWake_core1 );

            if( xTimeToWake_core1 < xConstTickCount_core1 )
            {
                /* Wake time has overflowed.  Place this item in the overflow
                 * list. */
                vListInsert_core1( pxOverflowDelayedTaskList_core1, &( pxCurrentTCB_core1->xStateListItem_core1 ) );
            }
            else
            {
                /* The wake time has not overflowed, so the current block list
                 * is used. */
                vListInsert_core1( pxDelayedTaskList_core1, &( pxCurrentTCB_core1->xStateListItem_core1 ) );

                /* If the task_core1 entering the blocked state was placed at the
                 * head of the list of blocked tasks then xNextTaskUnblockTime_core1
                 * needs to be updated too. */
                if( xTimeToWake_core1 < xNextTaskUnblockTime_core1 )
                {
                    xNextTaskUnblockTime_core1 = xTimeToWake_core1;
                }
                else
                {
                    mtCOVERAGE_TEST_MARKER_core1();
                }
            }
        }
    }
    #else /* INCLUDE_vTaskSuspend_core1 */
    {
        /* Calculate the time at which the task_core1 should be woken if the event
         * does not occur.  This may overflow but this doesn't matter, the kernel
         * will manage it correctly. */
        xTimeToWake_core1 = xConstTickCount_core1 + xTicksToWait_core1;

        /* The list item will be inserted in wake time order. */
        listSET_LIST_ITEM_VALUE_core1( &( pxCurrentTCB_core1->xStateListItem_core1 ), xTimeToWake_core1 );

        if( xTimeToWake_core1 < xConstTickCount_core1 )
        {
            /* Wake time has overflowed.  Place this item in the overflow list. */
            vListInsert_core1( pxOverflowDelayedTaskList_core1, &( pxCurrentTCB_core1->xStateListItem_core1 ) );
        }
        else
        {
            /* The wake time has not overflowed, so the current block list is used. */
            vListInsert_core1( pxDelayedTaskList_core1, &( pxCurrentTCB_core1->xStateListItem_core1 ) );

            /* If the task_core1 entering the blocked state was placed at the head of the
             * list of blocked tasks then xNextTaskUnblockTime_core1 needs to be updated
             * too. */
            if( xTimeToWake_core1 < xNextTaskUnblockTime_core1 )
            {
                xNextTaskUnblockTime_core1 = xTimeToWake_core1;
            }
            else
            {
                mtCOVERAGE_TEST_MARKER_core1();
            }
        }

        /* Avoid compiler warning when INCLUDE_vTaskSuspend_core1 is not 1. */
        ( void ) xCanBlockIndefinitely_core1;
    }
    #endif /* INCLUDE_vTaskSuspend_core1 */
}

