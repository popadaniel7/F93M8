    static void prvDeleteTCB_core1( TCB_t_core1 * pxTCB_core1 )
    {
        /* This call is required specifically for the TriCore port.  It must be
         * above the vPortFree_core1() calls.  The call is also used by ports/demos that
         * want to allocate and clean RAM statically. */
        portCLEAN_UP_TCB_core1( pxTCB_core1 );

        #if ( ( configUSE_NEWLIB_REENTRANT_core1 == 1 ) || ( configUSE_C_RUNTIME_TLS_SUPPORT_core1 == 1 ) )
        {
            /* Free up the memory allocated for the task_core1's TLS Block. */
            configDEINIT_TLS_BLOCK_core1( pxCurrentTCB_core1->xTLSBlock_core1 );
        }
        #endif

        #if ( ( configSUPPORT_DYNAMIC_ALLOCATION_core1 == 1 ) && ( configSUPPORT_STATIC_ALLOCATION_core1 == 0 ) && ( portUSING_MPU_WRAPPERS_core1 == 0 ) )
        {
            /* The task_core1 can only have been allocated dynamically - free both
             * the stack and TCB. */
            vPortFreeStack_core1( pxTCB_core1->pxStack_core1 );
            vPortFree_core1( pxTCB_core1 );
        }
        #elif ( tskSTATIC_AND_DYNAMIC_ALLOCATION_POSSIBLE_core1 != 0 ) /*lint !e731 !e9029 Macro has been consolidated for readability reasons. */
        {
            /* The task_core1 could have been allocated statically or dynamically, so
             * check what was statically allocated before trying to free the
             * memory. */
            if( pxTCB_core1->ucStaticallyAllocated_core1 == tskDYNAMICALLY_ALLOCATED_STACK_AND_TCB_core1 )
            {
                /* Both the stack and TCB were allocated dynamically, so both
                 * must be freed. */
                vPortFreeStack_core1( pxTCB_core1->pxStack_core1 );
                vPortFree_core1( pxTCB_core1 );
            }
            else if( pxTCB_core1->ucStaticallyAllocated_core1 == tskSTATICALLY_ALLOCATED_STACK_ONLY_core1 )
            {
                /* Only the stack was statically allocated, so the TCB is the
                 * only memory that must be freed. */
                vPortFree_core1( pxTCB_core1 );
            }
            else
            {
                /* Neither the stack nor the TCB were allocated dynamically, so
                 * nothing needs to be freed. */
                configASSERT_core1( pxTCB_core1->ucStaticallyAllocated_core1 == tskSTATICALLY_ALLOCATED_STACK_AND_TCB_core1 );
                mtCOVERAGE_TEST_MARKER_core1();
            }
        }
        #endif /* configSUPPORT_DYNAMIC_ALLOCATION_core1 */
    }

#endif /* INCLUDE_vTaskDelete_core1 */
